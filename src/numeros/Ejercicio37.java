package numeros;

public class Ejercicio37 {

	public static void main(String[] args) {
		//Se declara e inicializa la variable
	     int i = 1;
	     //Se utiliza un ciclo while para imprimir los valores de 1 a 100
	        while (i <= 100) {
	        	
	        	//Impresiòn de los valores
	            System.out.println(i);
	            i++;
	        }
	}

}
